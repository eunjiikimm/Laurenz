function setLanguage(lang) {
  localStorage.setItem("lang", lang);
  const t = translations[lang];

  document.getElementById("title").innerText = t.title;

  const menuLinks = document.querySelectorAll("#menu a");
  menuLinks.forEach((link, index) => {
    link.textContent = t.menu[index];
    const href = link.getAttribute("href").split("?")[0];
    link.setAttribute("href", `${href}?lang=${lang}`);
  });

  document.body.dir = lang === "fa" ? "rtl" : "ltr";

  // 페이지 구분
  const page = document.body.getAttribute("data-page");

  if (page === "Profil" && t.pages.about) {
    document.getElementById("about-text").innerHTML = t.pages.about.text;
  } else if (page === "Strafverteidigung" && t.pages.services) {
    document.getElementById("services-text").innerHTML = t.pages.services.text;
  } else if (page === "Kosten" && t.pages.kosten) {
    document.getElementById("kosten-text").innerHTML = t.pages.kosten.text;
  }  else if (page === "Kooperationspartner" && t.pages.partners) {
  document.getElementById("partners-text").innerHTML = t.pages.partners.text;
}
}
